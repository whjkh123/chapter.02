package com.javaex.ex02;

public class Point {
	
	private String name;
	private int point;
	
	
	public void setName(String p) {
		name = p;
	}
	
	public String getName() {
		return name;
	}
	
	public void setPoint(int i) {
		point = i;
	}
	
	public int getPoint() {
		return point;
	}
}
