package com.javaex.ex10;

public class MyMath {
	
	private static double pi = 3.14;

	public static int plus(int a, int b) {
		return a + b;
	}

	public static double plus(double a, double b) {
		return a + b;
	}
	
	public static double circle(double r) {
		return r * r * pi;
	}

}
